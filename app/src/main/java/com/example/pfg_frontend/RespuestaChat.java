package com.example.pfg_frontend;

public class RespuestaChat {
    private String tipo;
    private String respuesta;
    private String nombre;
    private String url;

    public String getTipo() {
        return tipo;
    }

    public String getRespuesta() {
        return respuesta;
    }

    public String getNombre() {
        return nombre;
    }

    public String getUrl() {
        return url;
    }
}